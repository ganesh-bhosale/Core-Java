package JavaBasics;

public class whileLoop {
    public static void main(String [] args) {
        int i = 1;
        while(i<5){
            System.out.println(i);
            i++;
        }
        System.out.println("--------");
        int a = 5;
        do{
            System.out.println(a);
            a--;
        }while(a>1);
    }
}
